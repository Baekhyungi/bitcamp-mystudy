package study.lang.operator;

public class Test01 {
  public static void main(String[] args) {
    // 정수
    System.out.println(100 + 24);
    System.out.println(100 - 24);
    System.out.println(100 * 24);
    System.out.println(100 / 24);
    System.out.println(100 % 24);
    // 부동 소수점
    System.out.println(100.0 + 24.0);
    System.out.println(100.0 - 24.0);
    System.out.println(100.0 * 24.0);
    System.out.println(100.0 / 24.0);
    System.out.println(100.0 % 24.0);
    // 정수와 부동 소수점의 연산
    System.out.println(100 / 24.0);
    System.out.println(100.0 / 24);


  }
}
